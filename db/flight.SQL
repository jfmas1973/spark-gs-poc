CREATE TABLE FLIGHT 
(
  FLIGHTID NUMBER NOT NULL, 
  YEAR VARCHAR2(10) NOT NULL, 
  Month VARCHAR2(10) NOT NULL, 
  DayofMonth VARCHAR2(10) NOT NULL, 
  DayOfWeek VARCHAR2(10) NOT NULL, 
  DepTime VARCHAR2(10) NOT NULL, 
  CRSDepTime VARCHAR2(10), 
  ArrTime VARCHAR2(10),
  CRSArrTime VARCHAR2(10), 
  UniqueCarrier VARCHAR2(10), 
  FlightNum VARCHAR2(10), 
  TailNum VARCHAR2(10), 
  ActualElapsedTime VARCHAR2(10), 
  CRSElapsedTime VARCHAR2(10), 
  AirTime VARCHAR2(10), 
  ArrDelay VARCHAR2(10), 
  DepDelay VARCHAR2(10), 
  Origin VARCHAR2(10), 
  Dest VARCHAR2(10),   
  Distance VARCHAR2(10), 
  TaxiIn VARCHAR2(10), 
  TaxiOut VARCHAR2(10), 
  Cancelled VARCHAR2(10), 
  CancellationCode VARCHAR2(10), 
  Diverted VARCHAR2(10), 
  CarrierDelay VARCHAR2(10), 
  WeatherDelay VARCHAR2(10), 
  NASDelay VARCHAR2(10), 
  SecurityDelay VARCHAR2(10), 
  LateAircraftDelay VARCHAR2(10),

  CONSTRAINT FLIGHT_PK PRIMARY KEY (FLIGHTID ) ENABLE 
);

CREATE TABLE FLIGHT_JSON 
(
  JSONID NUMBER NOT NULL, 
  CONTENT VARCHAR2(600) NOT NULL, 

  CONSTRAINT FLIGHT_JSON_PK PRIMARY KEY (JSONID ) ENABLE 
);

CREATE SEQUENCE FLIGHT_SEQ  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;
CREATE SEQUENCE FLIGHT_JSON_SEQ  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;

